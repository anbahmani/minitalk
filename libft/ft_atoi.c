#include "../includes/libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	signe;
	int nb;

	i = 0;
	signe = 1;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (ft_isdigit((int)str[i]))
	{
		nb = (nb + str[i] - 48);
		if (str[i + 1] && ft_isdigit(str[i + 1]))
			nb *= 10;
		i++;
	}
	return (nb * signe);
}
